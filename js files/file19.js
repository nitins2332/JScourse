// switch statement
let day = 4;

switch (day) {
  case 0:
    console.log("Sunday");
    break;
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console.log("Friday");
    break;
  case 5:
    console.log("Saterday");
    break;
  default:
    console.log("Invalid day");
}
